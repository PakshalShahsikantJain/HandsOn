//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

//Required Libraries
package main
import "fmt";

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

func Pattern(No int){

	//For as While loop in Golang
	for No > 0{
		fmt.Printf("*\t");
		No--;
	}
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

func main() {
	var No int = 0;

	fmt.Println("Jay Ganesh....");
	fmt.Println("Enter One Number....");
	fmt.Scanf("%d",&No);

	Pattern(No);
}